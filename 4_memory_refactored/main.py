def main():
    """
    主程序入口
    负责初始化对话历史，运行主循环
    """
    print("✓ 已加载初始记忆，开始对话（对话记录不会保存）")
    
    # 初始化角色设定
    role_prompt = get_role_prompt("zjy")
    
    # 初始化对话历史
    system_message = role_prompt + "\n\n" + get_break_rules()
    conversation_history = [{"role": "system", "content": system_message}]
    
    # 主循环
    while True:
        # 获取用户输入
        user_input = input("\n请输入你要说的话（输入\"再见\"退出）：")
        
        # 检查用户是否要退出
        if should_exit_by_user(user_input):
            print("对话结束")
            break
        
        # 进行一次对话
        try:
            reply = chat_once(conversation_history, user_input, role_prompt)
            
            # 显示AI回复（带ASCII头像）
            portrait = get_portrait()
            print(portrait + "\n" + reply)
            
            # 检查AI是否表示要结束
            if should_exit_by_ai(reply):
                print("\n对话结束")
                break
                
        except KeyboardInterrupt:
            # 用户按 Ctrl+C 中断程序
            print("\n\n程序被用户中断")
            break
        except Exception as e:
            # 其他异常（API调用失败、网络错误等）
            print(f"\n\n发生错误: {e}")
            break

if __name__ == "__main__":
    main()
